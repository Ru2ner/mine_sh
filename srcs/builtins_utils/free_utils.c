#include "exec.h"

void	*free_tab(char **tab)
{
	int	i;

	i = 0;
	if (!tab || !*tab)
		return (NULL);
	while (tab[i])
	{
		free(tab[i]);
		i++;
	}
	free(tab);
	return (NULL);
}
